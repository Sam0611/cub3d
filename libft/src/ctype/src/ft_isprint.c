int	ft_isprint(int c)
{
	if (c >= 65 && c <= 90)
		return (1);
	if (c >= 97 && c <= 122)
		return (2);
	if (c >= 48 && c <= 57)
		return (4);
	if ((c >= 33 && c <= 47) || (c >= 58 && c <= 96) || (c >= 123 && c <= 126))
		return (16);
	if (c == 32)
		return (64);
	return (0);
}
