#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (i < n && (unsigned char)str[i] != (unsigned char)c)
		i++;
	if (i == n)
		return (0);
	else
		return (str + i);
}
